#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	unsigned int i;

	if (!s)
		return ;
	i = 0;
	if (s && f)
	{
		while (s[i] != '\0')
		{
			f(s + i);
			i++;
		}
	}
}
