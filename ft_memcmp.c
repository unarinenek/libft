#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t	i;
	char	*str1;
	char	*str2;

	i = 0;
	str1 = (char *)s1;
	str2 = (char *)s2;
	while (i < n && str1[i] == str2[i])
		i++;
	if (i != n)
		return ((unsigned char)str1[i] - (unsigned char)str2[i]);
	else
		return (0);
}
